function Cell(x, y, val, color){
    this.x = x;
    this.y = y;
    this.val = val;
    this.color = color;
}

function cellify(grid, size){
    let cells = [[],[],[],[]];
    for(let i = 0; i < grid.length; i++){
        for(let j = 0; j < grid[i].length; j++){
            if(grid[i][j] == 1){
                cells[i].push(new Cell(j%size, Math.floor(j/size), 1, ""));
            }
        }
    }
    return cells;
}


